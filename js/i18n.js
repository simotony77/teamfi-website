function setLanguage(lang) {
  const dict = I18N[lang] || I18N.fr;
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });
  // Long-form content (legal pages): show only the block matching the language
  document.querySelectorAll("[data-lang-block]").forEach((block) => {
    block.style.display = block.dataset.langBlock === lang ? "block" : "none";
  });
  document.querySelectorAll(".lang-toggle__opt").forEach((opt) => {
    opt.classList.toggle("is-active", opt.dataset.lang === lang);
  });
  try { localStorage.setItem("teamfi-lang", lang); } catch (e) { /* private mode */ }
}

document.addEventListener("DOMContentLoaded", () => {
  let saved = "fr";
  try { saved = localStorage.getItem("teamfi-lang") || "fr"; } catch (e) { /* private mode */ }
  if (saved !== "fr") setLanguage(saved);

  document.getElementById("langToggle").addEventListener("click", () => {
    const current = document.documentElement.lang === "en" ? "en" : "fr";
    setLanguage(current === "fr" ? "en" : "fr");
  });
});
